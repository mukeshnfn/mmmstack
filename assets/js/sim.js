$(document).ready( function(){
	$('#chng').click(function(){
		$('#pro_cont').css({'display':"none"});
		$('#chang_cont').css({'display':'block'});
		$('#prod').removeClass('active');
		$('#prod').addClass('inactive');
		$('#chng').removeClass('inactive');
		$('#chng').addClass('active');
	});
	$('#prod').click(function(){
		$('#pro_cont').css({'display':"block"});
		$('#chang_cont').css({'display':'none'});
		$('#prod').removeClass('inactive');
		$('#prod').addClass('active');
		$('#chng').removeClass('active');
		$('#chng').addClass('inactive');
	});
	$('.comb_eye').click(function(){
		$('#back_blur').css({'display':'block'});
		$(this).parent().children('.eye_popup').css({'display':'block'});
		/*children('.eye_popup').css('display':'block');*/
	});
	$('#back_blur').click(function(){
		$('#back_blur').css({'display':'none'});
		$('.eye_popup').css({'display':'none'});
		$('.addCombo').css({'display':'none'});
		$('.createCombo').css({'display':'none'});
	});
	$('#AddNewCombo').click(function(){
		$('#back_blur').css({'display':'block'});
		$('.addCombo').css({'display':'block'});
	});
	$('#CreateNewCombo').click(function(){
		$('#back_blur').css({'display':'block'});
		$('.createCombo').css({'display':'block'});
	});
});
